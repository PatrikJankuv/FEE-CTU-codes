<h4>Ta i tak se da</h4>
<p>Určitě se bude hodně mluvit o zranění kickera Sebastiana Janikowského těsně před poločasem. Pro druhou půli rozehrával výkopy Seattlu drop kickem (pozn. vysvětleno v článku minulý týden) punter Michael Dickson. Místo extra pointů šel trenér Pete Carroll raději za dva, o field góly se naštěstí pokoušet nemusel. Jenže závěrečný pokus o onside kick obejít nijak nešlo. A pokus Dicksona byl hodně mizerný. Snad nejhorší, co jsem za poslední dobu viděl, bez šance pro jeho tým získat míč. Ale vymlouvat se na to nelze, získat onside kick je i tak velice nepravděpodobné a Seattle si to celé prohrál už dávno předtím.</p>

<p>Termíny duelov a súperov tímy spoznajú na jar, keď vedenie NFL zverejní rozpis zápasov. V Londýne sa aj v tejto sezóne odohrali štyri stretnutia, v Mexiku zápas medzi Los Angeles Rams a Kansas City Chiefs zrušili a presunuli do Los Angeles pre nevyhovujúci stav trávnika.</p>

